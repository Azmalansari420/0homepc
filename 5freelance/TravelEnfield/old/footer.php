    <script>
    document.addEventListener("DOMContentLoaded", function () {
    const video = document.getElementById("bannerVideo");
    const btn = document.getElementById("audioToggleBtn");

    if (btn && video) {
        btn.addEventListener("click", () => {
            if (video.muted) {
                video.muted = false;
                btn.innerHTML = "🔊";
            } else {
                video.muted = true;
                btn.innerHTML = "🔇";
            }
        });
    }
});
  



/* Gallery Videos */
document.querySelectorAll('.mute-btn').forEach(button => {

    button.addEventListener('click', async function () {

        let currentVideo = this.parentElement.querySelector('.gallery-video');
        let allVideos = document.querySelectorAll('.gallery-video');
        let allButtons = document.querySelectorAll('.mute-btn');

        /* agar already ON hai to OFF karo */
        if (!currentVideo.muted) {
            currentVideo.muted = true;
            currentVideo.volume = 0;
            this.innerHTML = '🔇 Mute';
            return;
        }

        /* sab OFF karo */
        allVideos.forEach(v => {
            v.muted = true;
            v.volume = 0;
        });

        allButtons.forEach(b => b.innerHTML = '🔇 Mute');

        /* sirf current ON */
        currentVideo.muted = false;
        currentVideo.volume = 1;

        try {
            await currentVideo.play();
        } catch(e) {}

        this.innerHTML = '🔊 Sound On';

    });

});

</script>
